# Input two numbers with decimals (fractions) and display the average with decimals.

# Inputing the two float numbers
float1 = float(input("Enter float number: "))
float2 = float(input("Enter another float number: "))

# Calculating the average of the two values assigned to the variables
avg = (float1 + float2)/2

# Printing the average
print("Average is :- ", avg)