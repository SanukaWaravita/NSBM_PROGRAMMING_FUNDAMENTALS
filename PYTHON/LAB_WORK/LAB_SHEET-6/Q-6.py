# Consider the value of π (pi) as 3.141592. Using the round() function in Python, write a simple program to determine the output after rounding off the value of π to different numbers of decimals, ranging from 0 to 5. 

pi = 3.141592

print(round(pi))
print(round(pi, 1))
print(round(pi, 2))
print(round(pi, 3))
print(round(pi, 4))
print(round(pi, 5))